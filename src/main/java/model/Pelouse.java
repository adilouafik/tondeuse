package model;

public class Pelouse {
    int dimenssionA;
    int dimenssionB;

    public Pelouse(int dimenssionA, int dimenssionB) {
        this.dimenssionA = dimenssionA;
        this.dimenssionB = dimenssionB;
    }

    public int getDimenssionA() {
        return dimenssionA;
    }

    public int getDimenssionB() {
        return dimenssionB;
    }
}
